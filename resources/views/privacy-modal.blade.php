            <h2>How your information is treated</h2>
            <p>FlowGig is a tool to help you organize your musician information assets. While you can keep your assets
                totally private, the most value of FlowGig is gained through collaborating with other FlowGig users in
                bands. Band members share equal access to the songs, gigs, setlists, links, memberships and any other
                assets connected to a band in which they are members.</p>
            <h3>Your personal information</h3>
            <ul>
                <li>The name you provide when you register is displayed, for other logged in users, on assets you
                    create or update, as "Created by ..." and "Updated by ..." and on the "Invite new band member"
                    page. You may change it on your account page. A changed name will be used on any existing as
                    well as any new asset you create or update.
                </li>
                <li>Your e-mail address is used to send you an account verification link and, if you forget your
                    password, reset links. It is visible to other logged in users on the "Invite new band member" page.
                </li>
                <li>Your password is transferred encrypted and stored hashed, treated to never be human readable.
                </li>
            </ul>
            <h3>Your band information</h3>
            <p>Every asset connected to a band (gigs, songs, setlists, links, etc.) is only accessible to the
                members of that band by default. However, band members can choose to define gigs for the band as
                "Public", making information about the gig viewable to the public through an open API.
            </p>
            <h3>Meta information</h3>
            <p>The analytics platform <a href="https://matomo.org/" title="Matomo Analytics Platform" target="_blank">
                    Matomo</a> is used to help the
                <a href="https://github.com/orgs/flowgig/people" title="FlowGig developers" target="_blank">
                    FlowGig developers</a> better understand how FlowGig is used, to improve functionality and user
                experience. Technical aspects, such as page navigation patterns, are measured - not personal information
                or the content of any user created assets.</p>
            <p>Cookies are used merely to keep you logged in during your work session.</p>
            <br />
            <p><b>FlowGig does not share any of your data with third parties.</b></p>
